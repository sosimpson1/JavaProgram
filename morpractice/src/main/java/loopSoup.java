public class loopSoup {

    public static void main(String args[]){
        int i=5;
        int j;

        while(i>0){
            System.out.println(i);
            i--;
            j=1;
            while (j <4 ){
                System.out.println(j);
                j++;
            }
            System.out.println();
        }
    }
}
